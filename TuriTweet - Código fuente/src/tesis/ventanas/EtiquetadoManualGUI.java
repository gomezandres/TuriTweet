    private void etiquetarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_etiquetarActionPerformed
        try {
            etiquetas[0] = this.bs.isSelected();
            etiquetas[1] = this.cal.isSelected();
            etiquetas[2] = this.cat.isSelected();
            etiquetas[3] = this.sal.isSelected();
            etiquetas[4] = this.tf.isSelected();
            etiquetas[5] = this.ng.isSelected();
            etiquetas[6] = this.indefinido.isSelected();
            polaridad[0] = this.pos.isSelected();
            polaridad[1] = this.neg.isSelected();
            polaridad[2] = this.neu.isSelected();
            this.tweet.setStatus(this.tweetsArea.getText());
            this.idTweet.setText(this.tweet.getId().toString());
            controladora.getRecolector().etiquetarTweets("tweets", this.tweet, this.etiquetas, this.polaridad, "2");
            totalesEtiquetas = controladora.getRecolector().contarEtiquetados();
            this.contadorBsAs.setText(totalesEtiquetas[0]);
            this.contadorCal.setText(totalesEtiquetas[1]);
            this.contadorCat.setText(totalesEtiquetas[2]);
            this.contadorSalt.setText(totalesEtiquetas[3]);
            this.contadorTdf.setText(totalesEtiquetas[4]);
            this.contadorNg.setText(totalesEtiquetas[5]);
            this.contadorTot.setText(totalesEtiquetas[6]);
            contar++;
            //this.valClasificados.setText(contar.toString());
            this.bs.setSelected(false);
            this.cal.setSelected(false);
            this.cat.setSelected(false);
            this.sal.setSelected(false);
            this.tf.setSelected(false);
            this.ng.setSelected(false);
            this.indefinido.setSelected(false);
            this.pos.setSelected(false);
            this.neg.setSelected(false);
            this.neu.setSelected(false);
            this.bs.setEnabled(true);
            this.cal.setEnabled(true);
            this.cat.setEnabled(true);
            this.sal.setEnabled(true);
            this.tf.setEnabled(true);
            this.ng.setEnabled(true);
            this.indefinido.setEnabled(true);
            this.pos.setEnabled(true);
            this.neg.setEnabled(true);
            this.neu.setEnabled(true);
            this.polaridadPanel.setEnabled(true);
            this.tweet = controladora.getRecolector().leerTweets("tweets");
            this.username.setText(tweet.getUsername());
            this.fechaLocal.setText(tweet.getLocaldate());
            this.fechaUtc.setText(tweet.getUtcdate());
            this.location.setText(tweet.getLocation());
            this.timezone.setText(tweet.getTimezone());
            this.tweetsArea.setText(tweet.getStatus());
        } catch (Exception ex) {
            Logger.getLogger(EtiquetadoManualGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_etiquetarActionPerformed

