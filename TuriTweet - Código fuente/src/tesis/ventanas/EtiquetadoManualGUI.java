    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        try {
            controladora.getRecolector().eliminarTweet("tweets", this.tweet.getId().toString());
            contarEliminados++;
            //this.valEliminados.setText(contarEliminados.toString());
            this.tweet = controladora.getRecolector().leerTweets("tweets");
            this.tweetsArea.setText(tweet.getStatus());
            this.idTweet.setText(this.tweet.getId().toString());
            this.username.setText(tweet.getUsername());
            this.fechaLocal.setText(tweet.getLocaldate());
            this.fechaUtc.setText(tweet.getUtcdate());
            this.location.setText(tweet.getLocation());
            this.timezone.setText(tweet.getTimezone());
            this.contadorTotGral.setText(controladora.getRecolector().contarTotal());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_eliminarActionPerformed

